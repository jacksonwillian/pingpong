
class Base {
    /**
    * @param  x
    * @param  y
    * @param  width
    * @param  height
    * @param  type
    * @param  verticalDirection
    * @param  horizontalDirection
    * @param  speed
    */
    constructor(x, y, width, height, type, verticalDirection, horizontalDirection, speed) {
        this.x                     = x;
        this.y                     = y;
        this.width                 = width;
        this.height                = height;
        this.type                  = type;
        this.verticalDirection     = verticalDirection;
        this.horizontalDirection   = horizontalDirection;
        this.speed                 = speed;
    }

}

